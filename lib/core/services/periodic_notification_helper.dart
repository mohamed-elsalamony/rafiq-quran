  static Future<void> showNotification({
    required int id,
    required String title,
    required String body,
  }) async {
    final FlutterLocalNotificationsPlugin localNotifications = FlutterLocalNotificationsPlugin();
    
    const AndroidInitializationSettings androidSettings =
        AndroidInitializationSettings('ic_launcher');

    const DarwinInitializationSettings iosSettings = DarwinInitializationSettings();

    const InitializationSettings initSettings = InitializationSettings(
      android: androidSettings,
      iOS: iosSettings,
    );

    // Initialize inside background isolate context
    await localNotifications.initialize(settings: initSettings);

    const AndroidNotificationDetails androidDetails = AndroidNotificationDetails(
      'periodic_channel_id',
      'التذكير الدوري بالذكر والآية',
      channelDescription: 'تنبيهات الذكر والآيات والحديث الدوري التلقائي',
      importance: Importance.max,
      priority: Priority.high,
      playSound: true,
    );

    const NotificationDetails platformDetails = NotificationDetails(
      android: androidDetails,
      iOS: DarwinNotificationDetails(presentAlert: true, presentSound: true),
    );

    await localNotifications.show(
      id: id,
      title: title,
      body: body,
      notificationDetails: platformDetails,
    );
  }

  static Map<String, String> getRandomContent(String type) {
    final random = Random();
    
    // Choose content type randomly if 'all' is selected
    String selectedType = type;
    if (type == 'all') {
      final roll = random.nextInt(3);
      if (roll == 0) selectedType = 'verse';
      else if (roll == 1) selectedType = 'dhikr';
      else selectedType = 'hadith';
    }

    if (selectedType == 'verse') {
      final index = random.nextInt(verses.length);
      return {'title': '📖 آية قرآنية للتأمل', 'body': verses[index]};
    } else if (selectedType == 'dhikr') {
      final index = random.nextInt(adhkar.length);
      return {'title': '📿 ذكر اليوم', 'body': adhkar[index]};
    } else {
      final index = random.nextInt(hadiths.length);
      return {'title': '📚 من الهدي النبوي', 'body': hadiths[index]};
    }
  }
